// GetPresetNames returns all available preset names sorted alphabetically.
func GetPresetNames() []string {
	names := make([]string, 0, len(presets))
	for name := range presets {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}

// GetPresetTLE parses and returns the TLE data for a named preset.
func GetPresetTLE(name string) ([]models.TLEData, error) {
	data, ok := presets[name]
	if !ok {
		return nil, fmt.Errorf("preset '%s' not found", name)
	}
	return ParseTLEString(data)
}
